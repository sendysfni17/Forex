package com.sendysofiani.forex;

public class RatesModel
{
    private double AWG, AZN, BAM, BBD, BDT, BGN, BHD, BIF, BMD, BND;

    public RatesModel() {}

    public double getAWG() { return AWG; }
    public void setAWG(double AWG) {this.AWG = AWG; }


    public double getAZN() { return AZN; }
    public void setAZN(double AZN) {this.AZN = AZN; }

    public double getBAM() { return BAM; }
    public void setBAM(double BAM) {this.BAM = BAM; }


    public double getBBD() { return BBD; }
    public void setBBD(double BBD) {this.BBD = BBD ; }

    public double getBDT() { return BDT; }
    public void setBDT(double BDT) {this.BDT = BDT; }


    public double getBGN() { return BGN; }
    public void setBGN(double AZN) {this.BGN = BGN; }

    public double getBHD() { return BHD; }
    public void setBHD(double BHD) {this.BHD = BHD; }


    public double getBIF() { return BIF; }
    public void setBIF(double BIF) {this.BIF = BIF; }

    public double getBMD() { return BMD; }
    public void setBMD(double BMD) {this.BMD = BMD; }


    public double getBND() { return BND; }
    public void setBND(double BND) {this.BND = BND; }
}
